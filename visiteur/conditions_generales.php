      <h4>
        Clause n° 6 : Retard de paiement
      </h4>
      <p>
        En cas de défaut de paiement total ou partiel des marchandises livrées à l'échéance, l'acheteur doit verser à la société Véville - CarShop une pénalité de retard égale à trois fois le taux de l'intérêt légal.
      </p>
      <p>
        Le taux de l'intérêt légal retenu est celui en vigueur au jour de la livraison des marchandises.
      </p>
